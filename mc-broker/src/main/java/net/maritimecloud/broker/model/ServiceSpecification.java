package net.maritimecloud.broker.model;


public class ServiceSpecification {

    String serviceId;
    String version;
    String description;
    OperationalService operationalService;

    public ServiceSpecification() {

    }

    public String getServiceId() {
        return serviceId;
    }

    public void setServiceId(String serviceId) {
        this.serviceId = serviceId;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public OperationalService getOperationalService() {
        return operationalService;
    }

    public void setOperationalService(OperationalService operationalService) {
        this.operationalService = operationalService;
    }

}
